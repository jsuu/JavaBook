package sec05.exam01;

public class Car {
	//멤버변수
	String model;
	int speed;
	
	//생성자
	Car(String model){
		this.model = model;		
	}
	
	//메소드
	void setSpeed(int speed){
		this.speed = speed;
	}
	
	void run(){
		for (int i = 10; i < 50; i+=10) {
			this.setSpeed(i);	// this생략가능.
			System.out.println(this.model + "가 달린다.(시속:" + this.speed + "km/h");
		}
	}
}
